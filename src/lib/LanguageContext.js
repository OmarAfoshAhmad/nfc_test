export function LanguageProvider({ children }) {
    // ğŸ›¡ï¸ Fix Hydration Mismatch: Always start with 'ar' (server-side default)
    const [language, setLanguage] = useState('ar');
    const [mounted, setMounted] = useState(false);

    useEffect(() => {
        // Now we are on the client
        const savedLang = localStorage.getItem('app_lang') || 'ar';
        setLanguage(savedLang);
        setMounted(true);

        document.dir = savedLang === 'ar' ? 'rtl' : 'ltr';
        document.lang = savedLang;
    }, []);

    useEffect(() => {
        if (mounted) {
            localStorage.setItem('app_lang', language);
            document.dir = language === 'ar' ? 'rtl' : 'ltr';
            document.lang = language;
        }
    }, [language, mounted]);

    const t = (key) => {
        return translations[language][key] || key;
    };

    const toggleLanguage = () => {
        setLanguage(prev => prev === 'ar' ? 'en' : 'ar');
    };

    // ğŸ›¡ï¸ Fix Hydration: DO NOT return null. Render children with a default state ('ar') 
    // and update once mounted to preserve server-side HTML.
    return (
        <LanguageContext.Provider value={{ language, setLanguage, t, toggleLanguage, dir: language === 'ar' ? 'rtl' : 'ltr' }}>
            {children}
        </LanguageContext.Provider>
    );
}

export const useLanguage = () => useContext(LanguageContext);
